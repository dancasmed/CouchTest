function dtoPlace(Name, Description, Type, CountryName, StateName, MunicipalityName, LocalityName, SettlementName, Code, CodeType, CodeDescription, ElasticObjectCreationDate, ElasticObjectCreatedBy, ElasticObjectUpdatedBy, ElasticObjectDeleted, ElasticObjectApprobationLevel, ElasticObjectLastUpdateDate)
{
	this.Name = Name;
	this.Description = Description;
	this.Type = Type;
	this.CountryName = CountryName;
	this.StateName = StateName;
	this.MunicipalityName = MunicipalityName;
	this.LocalityName = LocalityName;
	this.SettlementName = SettlementName;
	this.Code = Code;
	this.CodeType = CodeType;
	this.CodeDescription = CodeDescription;
	this.ElasticObjectCreationDate = ElasticObjectCreationDate;
	this.ElasticObjectCreatedBy = ElasticObjectCreatedBy;
	this.ElasticObjectUpdatedBy = ElasticObjectUpdatedBy;
	this.ElasticObjectDeleted = ElasticObjectDeleted;
	this.ElasticObjectApprobationLevel = ElasticObjectApprobationLevel;
	this.ElasticObjectLastUpdateDate = ElasticObjectLastUpdateDate;
}
