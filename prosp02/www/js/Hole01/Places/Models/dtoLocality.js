function dtoLocality(CountryName, StateName, MunicipalityName, Name, Code, Altitude, CartographyPaperCode, AreaType, TotalPopulation, TotalMalePopulation, TotalFemalePopulation, TotalHomes, ElasticObjectCreationDate, ElasticObjectCreatedBy, ElasticObjectUpdatedBy, ElasticObjectDeleted, ElasticObjectApprobationLevel, ElasticObjectLastUpdateDate)
{
	this.CountryName = CountryName;
	this.StateName = StateName;
	this.MunicipalityName = MunicipalityName;
	this.Name = Name;
	this.Code = Code;
	this.Altitude = Altitude;
	this.CartographyPaperCode = CartographyPaperCode;
	this.AreaType = AreaType;
	this.TotalPopulation = TotalPopulation;
	this.TotalMalePopulation = TotalMalePopulation;
	this.TotalFemalePopulation = TotalFemalePopulation;
	this.TotalHomes = TotalHomes;
	this.ElasticObjectCreationDate = ElasticObjectCreationDate;
	this.ElasticObjectCreatedBy = ElasticObjectCreatedBy;
	this.ElasticObjectUpdatedBy = ElasticObjectUpdatedBy;
	this.ElasticObjectDeleted = ElasticObjectDeleted;
	this.ElasticObjectApprobationLevel = ElasticObjectApprobationLevel;
	this.ElasticObjectLastUpdateDate = ElasticObjectLastUpdateDate;
}
