function dtoSubSector(SectorCode, Code, Title, Description, Includes, Excludes, ElasticObjectCreationDate, ElasticObjectCreatedBy, ElasticObjectUpdatedBy, ElasticObjectDeleted, ElasticObjectApprobationLevel, ElasticObjectLastUpdateDate)
{
	this.SectorCode = SectorCode;
	this.Code = Code;
	this.Title = Title;
	this.Description = Description;
	this.Includes = Includes;
	this.Excludes = Excludes;
	this.ElasticObjectCreationDate = ElasticObjectCreationDate;
	this.ElasticObjectCreatedBy = ElasticObjectCreatedBy;
	this.ElasticObjectUpdatedBy = ElasticObjectUpdatedBy;
	this.ElasticObjectDeleted = ElasticObjectDeleted;
	this.ElasticObjectApprobationLevel = ElasticObjectApprobationLevel;
	this.ElasticObjectLastUpdateDate = ElasticObjectLastUpdateDate;
}
