function dtoUser(ContactId, ContactType, Email, Password, Roles, ElasticObjectCreationDate, ElasticObjectCreatedBy, ElasticObjectUpdatedBy, ElasticObjectDeleted, ElasticObjectApprobationLevel, ElasticObjectLastUpdateDate)
{
	this.ContactId = ContactId;
	this.ContactType = ContactType;
	this.Email = Email;
	this.Password = Password;
	this.Roles = Roles;
	this.ElasticObjectCreationDate = ElasticObjectCreationDate;
	this.ElasticObjectCreatedBy = ElasticObjectCreatedBy;
	this.ElasticObjectUpdatedBy = ElasticObjectUpdatedBy;
	this.ElasticObjectDeleted = ElasticObjectDeleted;
	this.ElasticObjectApprobationLevel = ElasticObjectApprobationLevel;
	this.ElasticObjectLastUpdateDate = ElasticObjectLastUpdateDate;
}
